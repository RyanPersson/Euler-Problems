/*******************************************************************************************************************************************************************************
 ** Function: get_move
 ** Description: returns move for ta
 ** Parameters: none
 ** Preconditions: ta move needed
 ** Postconditions: ta move returned
*******************************************************************************************************************************************************************************/

char Ta::get_move() {
	int choice = std::rand() % 4;
	switch(choice) {
		case 4: return 'W';
		case 3: return 'A';
		case 2: return 'S';
		case 1: return 'D';
	}
}


